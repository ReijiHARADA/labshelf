export function getBooksByCategory(category: string): Book[] {
  return dummyBooks.filter(book => book.category === category);
}

export function getRecommendedBooks(): Book[] {
  return dummyBooks.filter(book => book.recommended);
}

export function getLatestBooks(): Book[] {
  return dummyBooks.filter(book => book.latestFlag);
}

export function getPopularBooks(limit = 10): Book[] {
  return [...dummyBooks]
    .sort((a, b) => b.popularityScore - a.popularityScore)
    .slice(0, limit);
}

export function searchBooks(query: string): Book[] {
  const lowerQuery = query.toLowerCase();
  return dummyBooks.filter(book =>
    book.title.toLowerCase().includes(lowerQuery) ||
    book.author.toLowerCase().includes(lowerQuery) ||
    book.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    book.category.toLowerCase().includes(lowerQuery) ||
    book.isbn.includes(query)
  );
}

export function getBookById(id: string): Book | undefined {
  return dummyBooks.find(book => book.id === id);
}

export function getAllCategories(): string[] {
  return [...new Set(dummyBooks.map(book => book.category))];
}

export function getAllTags(): string[] {
  const tags = dummyBooks.flatMap(book => book.tags);
  return [...new Set(tags)];
}
